    def coin_code(self, string):
        # TODO all the supported fiat coins
        string = string.lower()
        if string in ["eur", "euro", "euros"]:
            return "EUR"
        if string in ["rur", "ruble", "rubles"]:
            return "RUR"
        if string in ["gbp", "pound", "pounds", "british pound", "british "
                                                                 "pounds",
                      "british pound sterling"]:
            return "GBP"
        if string in ["usd", "american dollars", "american dollar",
                      "dollar", "dollars", "us dollar", "us dollars",
                      "u s dollar", "u s dollars", "u s d", "$"]:
            return "USD"
        if string in self.coins.keys():
            return self.coins[string]
        return None

