from adapt.intent import IntentBuilder
from mycroft.skills.core import MycroftSkill
import requests
import json

__author__ = 'jarbas'


class CryptocurrencySkill(MycroftSkill):
    def __init__(self):
        super(CryptocurrencySkill, self).__init__()
        self.currency = "eur"
        self.coins = {}
        self.get_coin_list()

    def initialize(self):
        for coin in self.coins.keys():
            flag = False
            for currency in ["eur", "rur", "ruble", "gbp", "pound", "usd",
                             "dollar", "u s d", "g b p", " r u r"]:
                if currency in coin:
                    self.register_vocabulary(coin, "TargetCurrency")
                    flag = True
            if not flag:
                self.register_vocabulary(coin, "CryptoCurrency")

        intent = IntentBuilder("CryptocoinPriceIntent").require(
            "PriceKeyword").optionally(
            "TargetCurrency").require("CryptoCurrency").build()
        self.register_intent(intent, self.handle_coin_price)

        intent = IntentBuilder("CryptocoinChangeIntent").require(
            "ChangeKeyword").optionally(
            "TargetCurrency").require("CryptoCurrency").build()
        self.register_intent(intent, self.handle_coin_change)

        intent = IntentBuilder("CryptocoinVolumeIntent").require(
            "VolumeKeyword").optionally(
            "TargetCurrency").require("CryptoCurrency").build()
        self.register_intent(intent, self.handle_coin_volume)

    def handle_coin_price(self, message):
        # TODO find other mechanism
        # hack to correctly trigger intent f you say "bitcoin price change"
        utterance = message.data["utterance"]
        if "change" in utterance or "move" in utterance or "increase" in \
                utterance or "decrease" in utterance:
            self.handle_coin_change(message)
            return
        if "volume" in utterance:
            self.handle_coin_volume(message)
            return
        coin = message.data.get("CryptoCurrency").lower()
        currency = message.data.get("TargetCurrency", self.currency)
        code = self.coins[coin]
        if not code:
            self.speak_dialog("bad_coin")
            return
        currency_code = self.coin_code(currency)
        if not currency_code:
            self.speak_dialog("bad_coin", {"coin": currency_code})
            return
        url = "https://api.cryptonator.com/api/ticker/" + code + "-" + currency_code
        r = requests.get(url)
        j = json.loads(r.text)
        data = j["ticker"]
        success = j["success"]
        if not success:
            error = j["error"]
            self.speak_dialog("cryptonator.error", {"error": error})
            return
        price = data["price"]
        base = data["base"]
        target = data["target"]
        self.speak_dialog("cryptonator.price", {"base": coin, "target":
            currency, "price": price})
        self.set_context("CryptoCurrency", base)
        self.set_context("TargetCurrency", target)

    def handle_coin_change(self, message):
        coin = message.data.get("CryptoCurrency").lower()
        currency = message.data.get("TargetCurrency", self.currency)
        code = self.coins[coin]
        if not code:
            self.speak_dialog("bad_coin", {"coin": coin})
            return
        currency_code = self.coin_code(currency)
        if not currency_code:
            self.speak_dialog("bad_coin", {"coin": currency_code})
            return
        url = "https://api.cryptonator.com/api/ticker/" + code + "-" + currency_code
        r = requests.get(url)
        j = json.loads(r.text)
        data = j["ticker"]
        success = j["success"]
        if not success:
            error = j["error"]
            self.speak_dialog("cryptonator.error", {"error": error})
            return
        base = data["base"]
        target = data["target"]
        past_hour_change = data["change"]
        self.speak_dialog("cryptonator.change", {"base": coin, "target":
            currency, "change": past_hour_change})
        self.set_context("CryptoCurrency", base)
        self.set_context("TargetCurrency", target)

